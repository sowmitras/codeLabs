package com.op;

public class RelationalOp {

	public static void main(String[] args) {
		
		int a=10;
		int b=50;
		
		System.out.println("A==B="+(a==b));
		
		System.out.println("A!=B="+(a!=b));
		
		System.out.println("A>B="+(a>b));
		
		System.out.println("A<B="+(a<b));
		
        System.out.println("A>=B="+(a>=b));
		
		System.out.println("A<=B="+(a<=b));

	}
	
	

}
